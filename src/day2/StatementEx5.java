package day2;
/*
 do {
 문장;
 문장;
 }while(조건);
 */

public class StatementEx5 {
public static void main(String[] args) {
	int i =1;
	do {
		System.out.println("3 * " + i + " = " +(3*i));
		i++; // 3단 출력 
	}while(i<10);// do while 문은 끝에  ; 사용 
}
	
//for문:몇번 실행 할지 미리 알 수 있을때 사용 제일 많이 사용
//while:특정한 조건에 따라서 할지말지를 결정지을때  
// do while문 : 한번은 실행하고 싶을때 
}
