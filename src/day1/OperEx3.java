package day1;
//shift 연산자
public class OperEx3 {
public static void main(String[] args) {
	byte b =10; 
	System.out.println(" b->>2:" + (b>>2));//오른쪽으로 2칸 이동 넘어가는건 버림 나누기 역할을함 0000 1010
	System.out.println(" b<<-2:" + (b<<2));//왼쪽으로 2칸 이동 넘어가는건 버림 곱하기 역할을 함 0010 1000
	System.out.println(" -----");//왼쪽으로 2칸 이동 넘어가는건 버림 곱하기
	
	//게임사에서 사용하는 연산 하드웨어사양 많이 탈때 씀
	

}
}
