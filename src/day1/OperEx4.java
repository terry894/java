//OperEx4시작
public class OperEx4 {
	//메인 메서드 시작	
public static void main(String[] args) {
	int a = 100; 
	int b = 20;
	 System.out.println("a>b :" + (a>b)); // true, false로 결과나옴 
	 System.out.println("a<b :" + (a<b)); // false
	 System.out.println("a<=b :" + (a<=b)); //false
	 System.out.println("a>=b :" + (a>=b)); //true
	 System.out.println("a==b :" + (a==b)); //false
	 System.out.println("a!=b :" + (a!=b)); //true
	 
}
}
